# data types in python

# 1. int == integer
a = 10
print(type(a))  # <class 'int'>

# 2. str
b = "Saurabh"  
print(type(b))   # <class 'str'>

# 3. Float
c = 1.05
print(type(c))  # <class 'float'>

# 4. Boolean
d = True
e = False
print(type(d))  # <class 'bool'>
print(type(e))  # <class 'bool'>


# 5. None === empty
f = None
print(type(f))  # <class 'NoneType'>
